class Multiplication:

    def __init__(self, multiplier):
        self.multiplier = multiplier
    
    def multiply(self, number):      
        return number * self.multiplier

class Addition:

    def __init__(self, add):
        self.addi = add
    
    def addify(self, number):      
        return number + self.addi
    
class Income:

    def __init__(self, inc):
        self.income = inc
    
    def incomify(self, number):      
        return number - self.income
    
class divid:

    def __init__(self, div):
        self.divi = div
    
    def dividfy(self, number):      
        return number / self.divi